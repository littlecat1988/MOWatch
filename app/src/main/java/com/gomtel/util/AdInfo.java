public class AdInfo {
    private String ad_id = null;
    private String ad_num = null;
    private String imag_name = null;
    private String ad_url = null;
    private String ad_type = null;
    private String ad_endtime = null;

    public String getAd_endtime() {
        return ad_endtime;
    }

    public void setAd_endtime(String ad_endtime) {
        this.ad_endtime = ad_endtime;
    }

    public String getAd_id() {
        return ad_id;
    }

    public void setAd_id(String ad_id) {
        this.ad_id = ad_id;
    }

    public String getAd_num() {
        return ad_num;
    }

    public void setAd_num(String ad_num) {
        this.ad_num = ad_num;
    }

    public String getAd_type() {
        return ad_type;
    }

    public void setAd_type(String ad_type) {
        this.ad_type = ad_type;
    }

    public String getAd_url() {
        return ad_url;
    }

    public void setAd_url(String ad_url) {
        this.ad_url = ad_url;
    }

    public String getImag_name() {
        return imag_name;
    }

    public void setImag_name(String imag_name) {
        this.imag_name = imag_name;
    }
}


